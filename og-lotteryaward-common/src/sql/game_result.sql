 DROP TABLE GAME_RESULT;
 CREATE TABLE GAME_RESULT
   (ID NUMBER(15,0) primary Key NOT NULL, 
    GAME_ID VARCHAR2(15),  
    ISSUE VARCHAR2(50), 
    CREATE_DATE DATE, 
    RESULT_DATE DATE, 
    RESULT VARCHAR2(4000), 
    LAST_RESULT_DATE DATE, 
    GAME_NAME VARCHAR2(4000)
   ) ;
   
    COMMENT ON COLUMN GAME_RESULT.GAME_ID IS '游戏Id';
    COMMENT ON COLUMN GAME_RESULT.ISSUE IS '游戏期数如重庆时时彩20150830001';
    COMMENT ON COLUMN GAME_RESULT.CREATE_DATE IS '创建时间';
    COMMENT ON COLUMN GAME_RESULT.RESULT_DATE IS '结果开奖时间';
    COMMENT ON COLUMN GAME_RESULT.RESULT IS '开解结果 号码之间用逗号隔开';
    COMMENT ON COLUMN GAME_RESULT.LAST_RESULT_DATE IS '上次开奖日期';
    COMMENT ON COLUMN GAME_RESULT.GAME_NAME IS '游戏名称';   
    CREATE SEQUENCE GAME_RESULT_ID_SEQ;
    
    
    